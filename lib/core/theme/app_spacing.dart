/// Compact spacing system - professional and minimal
class AppSpacing {
  AppSpacing._();

  // Base unit - smaller for compact design
  static const double baseUnit = 4.0;

  // Spacing values - more compact
  static const double xs = baseUnit; // 4px
  static const double sm = baseUnit * 2; // 8px
  static const double md = baseUnit * 3; // 12px
  static const double lg = baseUnit * 4; // 16px
  static const double xl = baseUnit * 6; // 24px
  static const double xxl = baseUnit * 8; // 32px

  // Specific spacing - compact
  static const double cardPadding = 12.0; // 12px
  static const double screenPadding = 16.0; // 16px
  static const double cardGap = 8.0; // 8px
  static const double sectionGap = 16.0; // 16px
  static const double buttonPadding = 12.0; // 12px
  static const double inputPadding = 12.0; // 12px

  // Border radius - smaller, more refined
  static const double radiusSmall = 6.0;
  static const double radiusMedium = 8.0;
  static const double radiusLarge = 12.0;
  static const double radiusXLarge = 16.0;
  static const double radiusRound = 999.0;

  // Icon sizes - compact
  static const double iconSmall = 16.0;
  static const double iconMedium = 20.0;
  static const double iconLarge = 24.0;
  static const double iconXLarge = 32.0;
}
